    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
     // 1. Validar que el campo del ID no esté vacío
    if (txtDetCodigo.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Debe seleccionar o buscar un detalle de planilla para eliminar.");
        return;
    }

    // 2. Confirmación de seguridad
    int respuesta = JOptionPane.showConfirmDialog(
        null, 
        "¿Está seguro de eliminar este registro de planilla?", 
        "Confirmar Eliminación", 
        JOptionPane.YES_NO_OPTION
    );

    if (respuesta == JOptionPane.YES_OPTION) {
        try {
            // Instanciar la entidad y el ID a eliminar
            Controlador.controladorPlanilla.clsPlanillaDetalle detalle = new Controlador.controladorPlanilla.clsPlanillaDetalle();
            detalle.setDetcodigo(Integer.parseInt(txtDetCodigo.getText().trim()));

            Modelo.modeloPlanilla.PlanillaDetalleDAO dao = new Modelo.modeloPlanilla.PlanillaDetalleDAO();
            Controlador.clsBitacora bitacora = new Controlador.clsBitacora();

            // 3. Ejecutar la eliminación en la base de datos
            int resultado = dao.eliminarPlanillaDetalle(detalle, bitacora);

            if (resultado > 0) {
                JOptionPane.showMessageDialog(null, "Registro de planilla eliminado correctamente.");

                // 4. Limpiar campos y refrescar la tabla visual
                limpiarCampos(); 
                llenarTabla();   

                // 5. Resetear estados de los campos y botones
                txtPlaCodigo.setEnabled(false);
                txtEmpCodigo.setEnabled(false);
                txtSalario.setEnabled(false);
                txtPercepciones.setEnabled(false);
                txtDeducciones.setEnabled(false);
                
                btnGuardar.setEnabled(false);
                btnModificar.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnNuevo.setEnabled(true);
                
                txtBusqueda.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar el registro. Verifique si el ID existe.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El código detectado no es un número válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

