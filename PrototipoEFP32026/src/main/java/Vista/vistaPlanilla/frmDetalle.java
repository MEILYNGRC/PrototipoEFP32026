    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

      try {
        // 1. VALIDAR QUE EL CAMPO DE BÚSQUEDA NO ESTÉ VACÍO
        if (txtBusqueda.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un código para buscar");
            txtBusqueda.requestFocus();
            return;
        }

        // 2. OBTENER EL ID DEL CAMPO DE TEXTO
        int id = Integer.parseInt(txtBusqueda.getText().trim());

        // 3. INSTANCIAR DAO Y BITÁCORA
        Modelo.modeloPlanilla.PlanillaDetalleDAO dao = new Modelo.modeloPlanilla.PlanillaDetalleDAO();
        Controlador.clsBitacora bitacora = new Controlador.clsBitacora();

        // 4. EJECUTAR LA BÚSQUEDA
        Controlador.controladorPlanilla.clsPlanillaDetalle detalle = dao.obtenerPlanillaDetallePorId(id, bitacora);

        // 5. VALIDAR RESULTADO Y LLENAR CAMPOS
        if (detalle != null) {
            txtDetCodigo.setText(String.valueOf(detalle.getDetcodigo()));
            txtPlaCodigo.setText(String.valueOf(detalle.getPlacodigo()));
            txtEmpCodigo.setText(String.valueOf(detalle.getEmpcodigo()));
            txtSalario.setText(String.valueOf(detalle.getDetsalario()));
            txtPercepciones.setText(String.valueOf(detalle.getDettotalpercepciones()));
            txtDeducciones.setText(String.valueOf(detalle.getDettotaldeducciones()));
            txtLiquido.setText(String.valueOf(detalle.getDetliquido()));

            JOptionPane.showMessageDialog(null, "Registro encontrado");

        } else {
            // SI NO ENCUENTRA NADA, LIMPIAMOS LOS CAMPOS
            JOptionPane.showMessageDialog(null, "Detalle de planilla no encontrado");
            limpiarCampos(); // Llamamos al método que ya creamos antes
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Ingrese un código numérico válido");
        txtBusqueda.requestFocus();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

