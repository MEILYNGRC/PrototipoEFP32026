    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // 1. Validaciones básicas de campos vacíos
            if (txtPlaCodigo.getText().trim().isEmpty() || txtEmpCodigo.getText().trim().isEmpty() || txtSalario.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor, llene los campos obligatorios (Planilla, Empleado y Salario)");
                return;
            }

            // 2. Crear la entidad y setear los datos de los JTextFields
            clsPlanillaDetalle detalle = new clsPlanillaDetalle();

            detalle.setPlacodigo(Integer.parseInt(txtPlaCodigo.getText().trim()));
            detalle.setEmpcodigo(Integer.parseInt(txtEmpCodigo.getText().trim()));

            // Convertimos los montos de texto a double
            double sueldoBase = Double.parseDouble(txtSalario.getText().trim());
            double percepciones = txtPercepciones.getText().trim().isEmpty() ? 0 : Double.parseDouble(txtPercepciones.getText().trim());
            double deducciones = txtDeducciones.getText().trim().isEmpty() ? 0 : Double.parseDouble(txtDeducciones.getText().trim());

            // 3. Cálculo del Líquido (Operación lógica)
            double liquido = sueldoBase + percepciones - deducciones;

            // Asignamos al objeto
            detalle.setDetsalario(sueldoBase);
            detalle.setDettotalpercepciones(percepciones);
            detalle.setDettotaldeducciones(deducciones);
            detalle.setDetliquido(liquido);

            // 4. Ejecutar la inserción mediante el DAO
            PlanillaDetalleDAO dao = new PlanillaDetalleDAO();
            clsBitacora bitacora = new clsBitacora(); // Manteniendo tu estándar de bitácora

            int resultado = dao.insertarPlanillaDetalle(detalle, bitacora);

            // 5. Respuesta al usuario
            if (resultado > 0) {
                JOptionPane.showMessageDialog(null, "Detalle de planilla guardado correctamente.\nLíquido calculado: " + liquido);

                // Actualizar la tabla y limpiar campos
              llenarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo guardar el registro");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error: Asegúrese de ingresar solo números en los campos de montos e IDs.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error inesperado: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

